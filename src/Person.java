/**
 * A Person class
 * <pre>
 *     <b>Domain</b>:
 *      personID: String -- The unique id for the person
 *      descendant: String -- This person's descendant, or null if none
 *      firstName: String -- Person's first name
 *      lastName: String -- Person's last name
 *      gender: char -- Either 'm' or 'f'
 *      father: String -- PersonID of this person's father, nullable
 *      mother: String -- PersonID of this person's mother, nullable
 *      spouse: String -- PersonID of this person's spouse, nullable
 * </pre>
 */
public class Person
{
    private String personID;
    private String descendant;
    private String firstName;
    private String lastName;
    private char gender;
    private String father;
    private String mother;
    private String spouse;


}
